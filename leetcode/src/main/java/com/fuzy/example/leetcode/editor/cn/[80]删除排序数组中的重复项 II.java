class Solution800 {
    public int removeDuplicates(int[] nums) {
        int n = nums.length;
        int slow = 0, fast = 1;//定义快慢指针
        int count = 0;
        while(fast < n){
            if(nums[fast] == nums[slow]){
                count++;//如果相邻的两个数相等，计数器+1
            }else{
                count = 0;// 如果不相等复位为0
            }
            if(count < 2){//如果计数器小于2，则保留
                slow++;//当前符合条件的数组的长度
                nums[slow] = nums[fast];
            }
            fast++;
        }
        return slow + 1;

    }
}
