class Solution0103 {
    public static void main(String[] args) {
        replaceSpaces("Mr John Smith   ",13);
    }

    public static String replaceSpaces(String S, int length) {
        char[] chars = new char[S.length()*3];
        int i;
        for (i = 0; i < length; i++) {
            if(S.charAt(i)=='0'){
                chars[i++] = '%';
                chars[i++] = '2';
                chars[i++] = '0';
            }else{
                chars[i] = S.charAt(i);
            }
        }
        return new String(chars);
    }
}
