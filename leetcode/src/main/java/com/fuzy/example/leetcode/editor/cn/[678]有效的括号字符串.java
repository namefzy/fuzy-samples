class Solution678 {

    Map<Character,Character> map = new HashMap<>();


    public boolean checkValidString(String s) {

        map.put(')','(');
        char[] chars = s.toCharArray();
        Stack<Character> stack = new Stack<>();
        char c = s.charAt(0);
        if(c==')'){
            return false;
        }

        for (int i = 1; i < chars.length; i++) {
            char aChar = chars[i];
            if(aChar==' '){
                continue;
            }
            stack.push(aChar);
        }
        return false;
    }
}
